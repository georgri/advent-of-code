def read_input(text):
    keys, locks = [], []
    buf = []
    for line in text.split('\n'):
        if not line:
            if not buf:
                continue
            # handle buf
            # print(buf[0])
            temp = []
            for col in zip(*buf):
                temp.append(col.count('#')  - 1)
            if '.' not in buf[0]: # key
                keys.append(temp)
            else:
                locks.append(temp)
                
            buf = []
        else:
            buf.append(line)

    return keys, locks

print(read_input(text_test))

def check(k, l):
    for i in range(len(k)):
        if k[i] + l[i] > 5:
            return False
        
    return True

def solve(text):
    res = 0
    keys, locks = read_input(text)
    for k in keys:
        for l in locks:
            if check(k, l):
                res += 1


    return res

