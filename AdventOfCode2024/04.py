def solve(text):
    inp = read_input(text)

    res = 0
    n, m = len(inp), len(inp[0])
    for x, row in enumerate(inp):
        for y, c in enumerate(row):
            if c != 'X':
                continue
            # search 8 ways
            for xo in (-1, 0, 1):
                for yo in (-1, 0, 1):
                    if xo == 0 and yo == 0:
                        continue

                    if not (0 <= x+xo*3 < n and 0 <= y+yo*3 < m):
                        continue

                    if inp[x+xo][y+yo] != 'M':
                        continue
                    if inp[x+2*xo][y+2*yo] != 'A':
                        continue
                    if inp[x+3*xo][y+3*yo] != 'S':
                        continue

                    res += 1
                
    return res


print(solve(text))



