dirs = {'^':(-1, 0), '>': (0, 1), '<': (0, -1), 'v': (1, 0)}

def make_move(field, move, x, y):
    xo, yo = dirs[move]
    # find first dot before wall
    i = 1
    while field[x+xo*i][y+yo*i] != '#':
        if field[x+xo*i][y+yo*i] not in 'O[]':
            while i > 0:
                field[x+xo*i][y+yo*i], field[x+xo*(i-1)][y+yo*(i-1)] = field[x+xo*(i-1)][y+yo*(i-1)], field[x+xo*i][y+yo*i]
                i -= 1 
            return x+xo, y+yo
        
        i += 1

    return x, y

def count_res(field):
    res = 0
    for i, row in enumerate(field):
        for j, c in enumerate(row):
            if c != 'O':
                continue
            res += i * 100 + j
    return res


def get_robot_pos(field):
    for i, row in enumerate(field):
        for j, c in enumerate(row):
            if c == '@':
                return i,j
            
    return None # break intentionally 

def solve(text):
    field, moves = read_input(text)

    x,y = get_robot_pos(field)

    for move in moves:
        x, y = make_move(field, move, x, y)

    res = count_res(field)

    for row in field:
        print(''.join(row))
    return res


