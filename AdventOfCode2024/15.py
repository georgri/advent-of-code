def adj(field, x,y,xo):
    c = field[x][y]
    yield (x+xo, y)
    if c == '[':
        yield (x,y+1)
    elif c == ']':
        yield (x,y-1)

def dfs(field, x, y, xo, visited):

    if (x,y) in visited:
        return True
    visited.add((x,y))
    
    for a,b in adj(field, x,y,xo):
        if (a,b) in visited:
            continue
        c = field[a][b]
        if field[a][b] == '#':
            return False
        
        if c not in '[]':
            continue
        
        res = dfs(field, a,b, xo, visited)
        if not res:
            return False
    
    return True
        


def make_move2(field, move, x, y):
    xo, yo = dirs[move]

    # old logic if move left or right
    if yo != 0:
        return make_move(field, move, x, y)

    # up and down cases
    # traverse with dfs all the affected box positions
    # if wall is affected => return immediately
    visited = set()
    ok = dfs(field, x, y, xo, visited)
    if not ok:
        return x,y

    affected = sorted(visited, key=lambda x: x[0]*(-xo))
    for a,b in affected:
        field[a][b], field[a+xo][b] = field[a+xo][b], field[a][b]

    return x+xo, y+yo

def count_res(field):
    res = 0
