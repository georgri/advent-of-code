def dfs2(m, x, y, visited, sideset):
    if (x,y) in visited:
        return 0

    visited.add((x,y))
    area = 1

    for xo, yo in ((-1,0), (0,-1), (1,0), (0,1)):
        if 0 <= x + xo < len(m) and 0 <= (y+yo) < len(m[0]) and m[x+xo][y+yo] == m[x][y]:
            area += dfs2(m, x+xo,y+yo,visited, sideset)
        else:
            sideset.add((x,y, xo, yo))

    return area


def calcSides(m, sideset):

    visited = set()

    res = 0
    for x,y,xo,yo in sideset:
        if (x,y,xo,yo) in visited:
            continue
        visited.add((x,y,xo,yo))

        res += 1
        for a,b in adj(m,x,y):
            io, jo = a-x,b-y
            while (a,b,xo,yo) in sideset:
                visited.add((a,b,xo,yo))
                a += io
                b += jo
    return res
            
            


