def adj(m, x,y):
    for xo, yo in ((-1,0), (0,-1), (1,0), (0,1)):
        if 0 <= x + xo < len(m) and 0 <= (y+yo) < len(m[0]):
            yield x+xo, y+yo

def dfs(m, i, j, visited):
    if (i,j) in visited:
        return 0, 0

    visited.add((i,j))
    area = 1
    perimeter = 4

    for a,b in adj(m, i, j):
        if m[a][b] != m[i][j]:
            continue
        perimeter -= 1
        ar, p = dfs(m, a,b,visited)
        area += ar
        perimeter += p

    return area, perimeter




